/**
 * Internationalization (i18n)
 * Configuración de idiomas para la app
 */

import { I18n } from 'i18n-js';
import * as Localization from 'expo-localization';

// Listeners para notificar cambios de idioma a TODOS los componentes
const languageListeners = new Set<() => void>();

/**
 * Suscribe un callback para cambios de idioma.
 * Retorna función para desuscribirse.
 */
export const subscribeToLanguageChanges = (listener: () => void): (() => void) => {
  languageListeners.add(listener);
  return () => languageListeners.delete(listener);
};

/**
 * Notifica a todos los suscriptores que el idioma cambió
 */
export const notifyLanguageChange = () => {
  languageListeners.forEach((listener) => listener());
};

